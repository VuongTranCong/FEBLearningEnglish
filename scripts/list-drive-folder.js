#!/usr/bin/env node
/**
 * Lists all files in a Google Drive folder (recursively) and writes daily/js/drive-map.js
 * with path → fileId for .mp4, .mp3, and .pdf files. Run from project root.
 *
 * Setup:
 * 1. Google Cloud Console: create project → enable "Google Drive API"
 * 2. Create a Service Account (APIs & Services → Credentials → Service account)
 * 3. Download JSON key, save as scripts/credentials.json (or set GOOGLE_APPLICATION_CREDENTIALS)
 * 4. Share your Drive folder with the service account email (Editor or Viewer)
 * 5. Run: node scripts/list-drive-folder.js
 *
 * Folder ID is read from daily/js/config.js (googleDriveFolderId) or pass as first arg.
 */

const fs = require('fs');
const path = require('path');

const FOLDER_ID = process.argv[2] || '1gk3RgwxvMAfwibv-pL-xYCGzRs0sc6XH';
const MEDIA_EXT = /\.(mp4|mp3|mkv|webm|pdf)$/i;

const credentialsPath = path.join(__dirname, 'credentials.json');
const useCredentials = fs.existsSync(credentialsPath);

async function main() {
  let drive;
  try {
    const { google } = require('googleapis');
    const authOpts = { scopes: ['https://www.googleapis.com/auth/drive.readonly'] };
    if (useCredentials) {
      authOpts.keyFile = credentialsPath;
    }
    const auth = new google.auth.GoogleAuth(authOpts);
    drive = google.drive({ version: 'v3', auth });
  } catch (e) {
    console.error('Run: npm install googleapis');
    if (!useCredentials) {
      console.error('Add scripts/credentials.json (service account key) or set GOOGLE_APPLICATION_CREDENTIALS.');
    }
    process.exit(1);
  }

  const pathToId = {};
  async function listInFolder(folderId, prefix = '') {
    let pageToken;
    do {
      const res = await drive.files.list({
        q: `'${folderId}' in parents and trashed = false`,
        fields: 'nextPageToken, files(id, name, mimeType)',
        pageSize: 200,
        pageToken: pageToken || undefined,
      });
      const files = res.data.files || [];
      for (const f of files) {
        const fullPath = prefix ? `${prefix}/${f.name}` : f.name;
        if (f.mimeType === 'application/vnd.google-apps.folder') {
          await listInFolder(f.id, fullPath);
        } else if (MEDIA_EXT.test(f.name)) {
          pathToId[fullPath] = f.id;
        }
      }
      pageToken = res.data.nextPageToken;
    } while (pageToken);
  }

  console.log('Listing folder', FOLDER_ID, '...');
  await listInFolder(FOLDER_ID);
  const count = Object.keys(pathToId).length;
  console.log('Found', count, 'media files.');

  const outPath = path.join(__dirname, '..', 'daily', 'js', 'drive-map.js');
  const lines = [
    '/**',
    ' * Google Drive file ID map: relative path → Drive file ID.',
    ' * Generated by: node scripts/list-drive-folder.js',
    ' */',
    'window.DRIVE_FILE_IDS = {',
    ...Object.entries(pathToId)
      .sort((a, b) => a[0].localeCompare(b[0]))
      .map(([p, id]) => `  "${p.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}": "${id}",`),
    '};',
  ];
  fs.writeFileSync(outPath, lines.join('\n') + '\n', 'utf8');
  console.log('Wrote', outPath);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
